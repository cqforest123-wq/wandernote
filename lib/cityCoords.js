export function normalizeCityName(city) {
  return String(city || '').trim();
}

export function getCityCoords(cityName) {
  if (!cityName) return null;
  const key = normalizeCityName(cityName);
  if (CITY_COORDS[key]) return CITY_COORDS[key];
  // 模糊匹配：旅程名包含城市名，或城市名包含旅程名
  const found = Object.keys(CITY_COORDS).find(k =>
    key.includes(k) || k.includes(key)
  );
  return found ? CITY_COORDS[found] : null;
}

export function haversineDistanceKm(from, to) {
  if (!from || !to) return null;
  const toRad = deg => (deg * Math.PI) / 180;
  const R = 6371;
  const dLat = toRad(to.lat - from.lat);
  const dLng = toRad(to.lng - from.lng);
  const lat1 = toRad(from.lat);
  const lat2 = toRad(to.lat);
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(lat1) * Math.cos(lat2) * Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

export function formatDistance(km) {
  if (km === null || km === undefined) return null;
  if (km < 1) return `${Math.round(km * 1000)}m`;
  if (km < 10) return `${km.toFixed(1)}km`;
  return `${Math.round(km)}km`;
}
